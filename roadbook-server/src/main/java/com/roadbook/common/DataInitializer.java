    private void seed(String name, String region, int days, int dist, String season,
                      int difficulty, List<String> tags, List<TemplateWaypoint> wps) {
        RouteTemplate t = new RouteTemplate();
        t.setName(name);
        t.setRegion(region);
        t.setTotalDays(days);
        t.setTotalDistance(dist);
        t.setBestSeason(season);
        t.setDifficulty(difficulty);
        t.setTags(toJson(tags));
        t.setUsageCount(0);
        t.setStatus(1);
        t = templateRepo.save(t);

        for (int i = 0; i < wps.size(); i++) {
            TemplateWaypoint wp = wps.get(i);
            wp.setTemplateId(t.getId());
            wp.setSortOrder(i + 1);
            waypointRepo.save(wp);
        }
    }

    private static TemplateWaypoint wp(int day, String type, String name,
                                        double lng, double lat, int stay, String tips) {
        TemplateWaypoint w = new TemplateWaypoint();
        w.setDayNumber(day);
        w.setPointType(type);
        w.setName(name);
        w.setLng(BigDecimal.valueOf(lng));
        w.setLat(BigDecimal.valueOf(lat));
        w.setStayDuration(stay);
        w.setTips(tips);
        return w;
    }

