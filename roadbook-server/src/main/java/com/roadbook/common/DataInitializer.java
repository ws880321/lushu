    private void poi(String name, String category, String province, String city,
                     double lng, double lat, double drive, double parking, double road,
                     int rv, int camping, int pet, int signal, String tips) {
        Poi p = new Poi();
        p.setName(name);
        p.setCategory(category);
        p.setProvince(province);
        p.setCity(city);
        p.setLng(BigDecimal.valueOf(lng));
        p.setLat(BigDecimal.valueOf(lat));
        p.setDriveScore(BigDecimal.valueOf(drive));
        p.setParkingScore(parking > 0 ? BigDecimal.valueOf(parking) : null);
        p.setRoadScore(road > 0 ? BigDecimal.valueOf(road) : null);
        p.setRvFriendly(rv);
        p.setCampingAllowed(camping);
        p.setPetFriendly(pet);
        p.setSignalQuality(signal > 0 ? signal : null);
        p.setSource("manual");
        p.setConfirmedCount(0);
        if (tips != null && !tips.isEmpty()) {
            // store tips pattern... we don't have a tips column on Poi entity
            // use address as tips placeholder
            p.setAddress(tips);
        }
        poiRepo.save(p);
    }

    private static String toJson(List<String> tags) {
        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < tags.size(); i++) {
            if (i > 0) sb.append(",");
            sb.append("\"").append(tags.get(i)).append("\"");
        }
        sb.append("]");
        return sb.toString();
    }
